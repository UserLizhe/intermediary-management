package com.example.intermediarymanagementboot.demo.entity;
// Role.java

import lombok.Data;


@Data
public class Role {

    private String id;
    private String roleName;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getRoleName() {
        return roleName;
    }

    public void setRoleName(String roleName) {
        this.roleName = roleName;
    }
}
